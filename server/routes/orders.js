import express from "express";
import Order from "../models/Order.js";
import { authMiddleware, isAdmin } from "../middleware/authMiddleware.js";
const router = express.Router();
import { optionalAuth } from "../middleware/optionalMiddleware.js";
/**
 * üõçÔ∏è –°—Ç–≤–æ—Ä–µ–Ω–Ω—è –Ω–æ–≤–æ–≥–æ –∑–∞–º–æ–≤–ª–µ–Ω–Ω—è
 */
router.post("/", optionalAuth, async (req, res) => {
  try {
    const { name, phone, size, fabricId, customSize, comment } = req.body;

    // üß© –ü–µ—Ä–µ–≤—ñ—Ä–∫–∞ –æ–±–æ–≤‚Äô—è–∑–∫–æ–≤–∏—Ö –ø–æ–ª—ñ–≤
    if (!name || !phone || !size || !fabricId) {
      return res.status(400).json({ message: "–í—Å—ñ –æ–±–æ–≤‚Äô—è–∑–∫–æ–≤—ñ –ø–æ–ª—è –º–∞—é—Ç—å –±—É—Ç–∏ –∑–∞–ø–æ–≤–Ω–µ–Ω—ñ" });
    }

    // üßæ –§–æ—Ä–º—É–≤–∞–Ω–Ω—è –∑–∞–º–æ–≤–ª–µ–Ω–Ω—è
    const newOrder = new Order({
      user: req.user?.id || null, 
      name,
      phone,
      size,
      fabric: fabricId,
      customSize: customSize || {},
      comment: comment || "", // ‚úÖ –∫–æ–º–µ–Ω—Ç–∞—Ä –∫–æ—Ä–∏—Å—Ç—É–≤–∞—á–∞
      status: "pending",
    });

    console.log("üì¶ –û—Ç—Ä–∏–º–∞–Ω–æ –∑–∞–º–æ–≤–ª–µ–Ω–Ω—è:", req.body);

    await newOrder.save();

    res.status(201).json({
      message: "‚úÖ –ó–∞–º–æ–≤–ª–µ–Ω–Ω—è —É—Å–ø—ñ—à–Ω–æ —Å—Ç–≤–æ—Ä–µ–Ω–æ",
      order: newOrder,
    });
  } catch (err) {
    console.error("‚ùå –ü–æ–º–∏–ª–∫–∞ –ø—Ä–∏ —Å—Ç–≤–æ—Ä–µ–Ω–Ω—ñ –∑–∞–º–æ–≤–ª–µ–Ω–Ω—è:", err);
    res.status(500).json({ message: "–ü–æ–º–∏–ª–∫–∞ —Å–µ—Ä–≤–µ—Ä–∞ –ø—Ä–∏ —Å—Ç–≤–æ—Ä–µ–Ω–Ω—ñ –∑–∞–º–æ–≤–ª–µ–Ω–Ω—è" });
  }
});

/**
 * üìã –û—Ç—Ä–∏–º–∞—Ç–∏ –≤—Å—ñ –∑–∞–º–æ–≤–ª–µ–Ω–Ω—è (–¥–ª—è –∞–¥–º—ñ–Ω–∫–∏)
 */
router.get("/", authMiddleware, isAdmin, async (req, res) => {
  try {
    const orders = await Order.find()
      .populate("fabric", "name pricePerMeter image")
      .sort({ createdAt: -1 });

    res.json(orders);
  } catch (err) {
    console.error("‚ùå –ü–æ–º–∏–ª–∫–∞ –ø—Ä–∏ –æ—Ç—Ä–∏–º–∞–Ω–Ω—ñ –∑–∞–º–æ–≤–ª–µ–Ω—å:", err);
    res.status(500).json({ message: "–ü–æ–º–∏–ª–∫–∞ –ø—Ä–∏ –æ—Ç—Ä–∏–º–∞–Ω–Ω—ñ –∑–∞–º–æ–≤–ª–µ–Ω—å" });
  }
});
router.get("/my", authMiddleware, async (req, res) => {
  try {
    const myOrders = await Order.find({ user: req.user.id })
      .populate("fabric", "name pricePerMeter image")
      .sort({ createdAt: -1 });

    res.json(myOrders);
  } catch (err) {
    console.error("‚ùå –ü–æ–º–∏–ª–∫–∞ –ø—Ä–∏ –æ—Ç—Ä–∏–º–∞–Ω–Ω—ñ –∑–∞–º–æ–≤–ª–µ–Ω—å –∫–æ—Ä–∏—Å—Ç—É–≤–∞—á–∞:", err);
    res.status(500).json({ message: "–ü–æ–º–∏–ª–∫–∞ —Å–µ—Ä–≤–µ—Ä–∞" });
  }
});
export default router;
